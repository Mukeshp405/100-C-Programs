// Taking two numbers from user and adding them.
#include <stdio.h>

int main()
{
    // Variable Decalaration
    int a, b;

    // Taking Input from user
    printf("Enter first number :- ");
    scanf("%d", &a);

    printf("Enter second number :- ");
    scanf("%d", &b);

    // Displaying result
    printf("The sum is %d\n", a + b);
    return 0;
}
